    <p>Naoto Ōshima (大島 直人?) est un designer japonais de jeu vidéo, ancien employé de Sega, principalement connu pour avoir modelé le design de Sonic et Eggman. Après avoir quitté Sonic Team, Ōshima fonde un studio de développement indépendant nommé Artoon.</p>
